def get_max_processing_timestamp_silver(spark, silver_exists):
    """Obtenir le timestamp de traitement maximum de la table Silver"""
    if not silver_exists:
        print("üÜï Premi√®re ex√©cution - traitement de toutes les donn√©es Bronze")
        return None
    
    try:
        max_timestamp_sql = """
            SELECT MAX(processing_timestamp) as max_timestamp
            FROM silver_customer_images
        """
        
        result = spark.sql(max_timestamp_sql).collect()
        
        if result and result[0].max_timestamp:
            max_timestamp = result[0].max_timestamp
            print(f"üìÖ Dernier timestamp trait√© en Silver: {max_timestamp}")
            return max_timestamp
        else:
            print("‚ÑπÔ∏è Aucun timestamp trouv√© en Silver - traitement complet")
            return None
            
    except Exception as e:
        print(f"‚ö†Ô∏è Erreur lors de la r√©cup√©ration du max timestamp: {str(e)}")
        return None

def check_image_id_uniqueness(spark):
    """V√©rifier l'unicit√© des image_id dans les nouvelles donn√©es et compter les doublons √† √©liminer"""
    try:
        duplicates_sql = """
            SELECT image_id, COUNT(*) as count
            FROM bronze_customer_images
            WHERE image_id IS NOT NULL AND TRIM(image_id) != ''
            GROUP BY image_id
            HAVING COUNT(*) > 1
            ORDER BY count DESC
            LIMIT 10
        """
        
        duplicates_df = spark.sql(duplicates_sql)
        duplicate_count = duplicates_df.count()
        
        if duplicate_count > 0:
            print(f"‚ö†Ô∏è ATTENTION: {duplicate_count} image_id avec doublons trouv√©s (seul le plus r√©cent sera conserv√©):")
            duplicates_df.show(10, truncate=False)
            
            # Calculer le nombre total de doublons √† √©liminer
            total_duplicates_sql = """
                SELECT SUM(count - 1) as total_to_remove
                FROM (
                    SELECT image_id, COUNT(*) as count
                    FROM bronze_customer_images
                    WHERE image_id IS NOT NULL AND TRIM(image_id) != ''
                    GROUP BY image_id
                    HAVING COUNT(*) > 1
                ) duplicates
            """
            
            result = spark.sql(total_duplicates_sql).collect()
            if result and result[0].total_to_remove:
                total_to_remove = result[0].total_to_remove
                print(f"üóëÔ∏è {total_to_remove} enregistrements dupliqu√©s seront √©limin√©s")
            
        else:
            print("‚úÖ Aucun image_id dupliqu√© trouv√©")
            
        return duplicate_count
        
    except Exception as e:
        print(f"‚ö†Ô∏è Erreur lors de la v√©rification des doublons: {str(e)}")
        return 0

def create_silver_table_sql(spark, silver_path, max_timestamp_silver):
    """Cr√©er la requ√™te SQL pour nettoyer et transformer les donn√©es customer images avec d√©duplication"""
    
    # Condition pour le traitement incr√©mental
    incremental_condition = ""
    if max_timestamp_silver:
        incremental_condition = f"AND processing_timestamp > '{max_timestamp_silver}'"
    
    # Requ√™te SQL avec d√©duplication par image_id (garde le plus r√©cent)
    cleaning_sql = f"""
        WITH ranked_data AS (
            SELECT 
                -- Nettoyage et validation du image_id (cl√© unique)
                CASE 
                    WHEN image_id IS NOT NULL AND TRIM(image_id) != '' 
                    THEN TRIM(image_id)
                    ELSE NULL 
                END as image_id,
                
                -- Nettoyage du s3_path - accepter les chemins relatifs aussi
                CASE 
                    WHEN s3_path IS NOT NULL AND TRIM(s3_path) != '' 
                    THEN TRIM(s3_path)
                    ELSE NULL 
                END as s3_path,
                
                -- Validation du customer_id
                CASE 
                    WHEN customer_id IS NOT NULL AND TRIM(customer_id) != '' 
                    THEN TRIM(customer_id)
                    ELSE NULL 
                END as customer_id,
                
                -- Utiliser order_id s'il existe, sinon extracted_order_id
                CASE 
                    WHEN order_id IS NOT NULL AND TRIM(order_id) != '' 
                    THEN TRIM(order_id)
                    WHEN extracted_order_id IS NOT NULL AND TRIM(extracted_order_id) != '' 
                    THEN TRIM(extracted_order_id)
                    ELSE NULL 
                END as order_id,
                
                processing_timestamp,
                upload_timestamp,
                partition_year,
                partition_month,
                partition_day,
                current_timestamp() as silver_load_timestamp,
                
                -- Ranger par processing_timestamp DESC pour garder le plus r√©cent
                ROW_NUMBER() OVER (
                    PARTITION BY TRIM(image_id) 
                    ORDER BY processing_timestamp DESC
                ) as rn
                
            FROM bronze_customer_images
            WHERE image_id IS NOT NULL
                AND TRIM(image_id) != ''
                AND processing_timestamp IS NOT NULL
                AND partition_year IS NOT NULL
                AND partition_month IS NOT NULL
                AND partition_day IS NOT NULL
                {incremental_condition}
        )
        SELECT 
            image_id,
            s3_path,
            customer_id,
            order_id,
            processing_timestamp,
            upload_timestamp,
            partition_year,
            partition_month,
            partition_day,
            silver_load_timestamp
        FROM ranked_data
        WHERE rn = 1
    """
    
    return cleaning_sql

def execute_bronze_to_silver_transformation(spark, bronze_path, silver_path):
    """Ex√©cuter la transformation compl√®te Bronze vers Silver pour Customer Images"""
    
    print("=== D√âMARRAGE DE LA TRANSFORMATION BRONZE VERS SILVER CUSTOMER IMAGES ===")
    
    # 1. Enregistrer les tables Delta
    silver_exists = register_delta_tables(spark, bronze_path, silver_path)
    
    # 2. V√©rifier l'unicit√© des image_id
    duplicate_count = check_image_id_uniqueness(spark)
    
    # 3. Obtenir le timestamp maximum de Silver pour traitement incr√©mental
    max_timestamp_silver = get_max_processing_timestamp_silver(spark, silver_exists)
    
    # 4. Cr√©er la requ√™te SQL de nettoyage
    cleaning_sql = create_silver_table_sql(spark, silver_path, max_timestamp_silver)
    
    print("üìã Requ√™te SQL de transformation cr√©√©e")
    print("üîç V√©rification des nouvelles donn√©es √† traiter...")
    
    # 5. Ex√©cuter la requ√™te et compter les r√©sultats
    try:
        cleaned_df = spark.sql(cleaning_sql)
        new_records_count = cleaned_df.count()
        
        if new_records_count == 0:
            print("‚úÖ Aucune nouvelle donn√©e √† traiter - Silver est √† jour")
            return
        
        print(f"üìä {new_records_count} nouveaux enregistrements √† traiter")
        
        # 6. V√©rifier l'unicit√© des image_id dans les donn√©es nettoy√©es
        print("\nüîç V√âRIFICATION DE L'UNICIT√â DES IMAGE_ID APR√àS NETTOYAGE:")
        unique_images_sql = """
            SELECT COUNT(DISTINCT image_id) as unique_images,
                   COUNT(*) as total_records
            FROM (
                SELECT * FROM (""" + cleaning_sql + """) as cleaned_data
            ) t
        """
        
        unique_result = spark.sql(unique_images_sql).collect()[0]
        unique_images = unique_result.unique_images
        total_records = unique_result.total_records
        
        print(f"üìä Images uniques: {unique_images}")
        print(f"üìä Total enregistrements: {total_records}")
        
        if unique_images == total_records:
            print("‚úÖ Tous les image_id sont maintenant uniques apr√®s d√©duplication")
        else:
            print(f"‚ùå ERREUR: D√©duplication √©chou√©e - {total_records - unique_images} doublons restants")
        
        # 7. Afficher un √©chantillon des donn√©es nettoy√©es
        print("\nüîç √âCHANTILLON DES DONN√âES NETTOY√âES:")
        sample_df = cleaned_df.limit(5)
        sample_df.select("image_id", "s3_path", "customer_id", "order_id").show(truncate=False)
        
        # 8. V√©rifier la qualit√© des donn√©es nettoy√©es
        print("\nüìä QUALIT√â DES DONN√âES APR√àS NETTOYAGE:")
        
        # Compter les valeurs nulles pour les champs critiques
        null_image_id = cleaned_df.filter(col("image_id").isNull()).count()
        null_s3_path = cleaned_df.filter(col("s3_path").isNull()).count()
        null_customer_id = cleaned_df.filter(col("customer_id").isNull()).count()
        null_order_id = cleaned_df.filter(col("order_id").isNull()).count()
        
        print(f"üñºÔ∏è Image ID manquants/invalides: {null_image_id}/{new_records_count}")
        print(f"üìÅ Chemins S3 manquants/invalides: {null_s3_path}/{new_records_count}")
        print(f"üë§ Customer ID manquants/invalides: {null_customer_id}/{new_records_count}")
        print(f"üì¶ Order ID manquants/invalides: {null_order_id}/{new_records_count}")
        
        # 9. √âcrire les donn√©es dans la table Silver
        print(f"\nüíæ √âcriture de {new_records_count} enregistrements vers Silver...")
        
        if silver_exists:
            print("üìä Mode APPEND - Ajout des nouvelles donn√©es")
            
            cleaned_df.write \
                .format("delta") \
                .mode("append") \
                .partitionBy("partition_year", "partition_month", "partition_day") \
                .option("mergeSchema", "true") \
                .save(silver_path)
        else:
            print("üÜï Mode OVERWRITE - Cr√©ation de la nouvelle table Silver")
            
            cleaned_df.write \
                .format("delta") \
                .mode("overwrite") \
                .partitionBy("partition_year", "partition_month", "partition_day") \
                .option("delta.autoOptimize.optimizeWrite", "true") \
                .option("delta.autoOptimize.autoCompact", "true") \
                .save(silver_path)
        
        # 10. V√©rification finale
        print("\n‚úÖ TRANSFORMATION TERMIN√âE AVEC SUCC√àS")
        
        # Comptage final et v√©rification des image_id uniques en Silver
        silver_final_df = spark.read.format("delta").load(silver_path)
        total_silver_count = silver_final_df.count()
        
        # V√©rifier l'unicit√© finale en Silver
        silver_final_df.createOrReplaceTempView("final_silver")
        final_unique_sql = """
            SELECT COUNT(DISTINCT image_id) as unique_images,
                   COUNT(*) as total_records
            FROM final_silver
        """
        
        final_result = spark.sql(final_unique_sql).collect()[0]
        final_unique_images = final_result.unique_images
        final_total_records = final_result.total_records
        
        print(f"üìà Total des enregistrements en Silver: {total_silver_count:,}")
        print(f"üîë Images uniques en Silver: {final_unique_images:,}")
        
        if final_unique_images == final_total_records:
            print("‚úÖ Tous les image_id sont uniques en Silver - D√©duplication r√©ussie!")
        else:
            print(f"‚ùå ERREUR: {final_total_records - final_unique_images} doublons pr√©sents en Silver")
        
        # 11. Statistiques business
        print("\nüìä STATISTIQUES BUSINESS CUSTOMER IMAGES:")
        
        # V√©rification des s3_path non-null
        s3_non_null_sql = """
            SELECT 
                COUNT(*) as total,
                SUM(CASE WHEN s3_path IS NOT NULL THEN 1 ELSE 0 END) as s3_path_present,
                SUM(CASE WHEN s3_path IS NULL THEN 1 ELSE 0 END) as s3_path_missing
            FROM final_silver
        """
        
        s3_stats = spark.sql(s3_non_null_sql).collect()[0]
        print(f"üìÅ S3 Paths pr√©sents: {s3_stats.s3_path_present}/{s3_stats.total}")
        print(f"üìÅ S3 Paths manquants: {s3_stats.s3_path_missing}/{s3_stats.total}")
        
        # Nombre de clients uniques
        customer_stats_sql = """
            SELECT COUNT(DISTINCT customer_id) as unique_customers
            FROM final_silver
            WHERE customer_id IS NOT NULL
        """
        customer_result = spark.sql(customer_stats_sql).collect()[0]
        print(f"üë• Clients uniques: {customer_result.unique_customers}")
        
        # Nombre de commandes uniques
        order_stats_sql = """
            SELECT COUNT(DISTINCT order_id) as unique_orders
            FROM final_silver
            WHERE order_id IS NOT NULL
        """
        order_result = spark.sql(order_stats_sql).collect()[0]
        print(f"üì¶ Commandes uniques: {order_result.unique_orders}")
        
        print(f"\nüéâ TRANSFORMATION BRONZE -> SILVER CUSTOMER IMAGES COMPL√âT√âE!")
        print(f"üìç Table Silver disponible √†: {silver_path}")
        print(f"üìä {new_records_count:,} nouveaux enregistrements ajout√©s")
        print(f"üìà Total des enregistrements: {total_silver_count:,}")
        print(f"üîë Images uniques: {final_unique_images:,}")
        
        # R√©sum√© des colonnes conserv√©es
        print(f"\nüìã COLONNES CONSERV√âES EN SILVER:")
        print("   1. image_id (unique identifier)")
        print("   2. s3_path")
        print("   3. customer_id")
        print("   4. order_id")
        print("   5. processing_timestamp")
        print("   6. upload_timestamp")
        print("   7. partition_year")
        print("   8. partition_month")
        print("   9. partition_day")
        print("  10. silver_load_timestamp")
        
    except Exception as e:
        print(f"‚ùå Erreur lors de la transformation: {str(e)}")
        import traceback
        traceback.print_exc()
        raise e

